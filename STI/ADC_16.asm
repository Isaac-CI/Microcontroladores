	BSF	GPIO,0		;CASO A TENSÃO SEJA MAIOR QUE 4.6875V, "SETA" GP0, POIS A TENSÃO É MAIOR QUE 4.6875V.
	
	
	
	
;* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * *
;*                 ROTINA DE SAÍDA DA INTERRUPÇÃO                  *
