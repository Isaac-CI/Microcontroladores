	MOVF	ADRESH,0        ;ESCREVE EM WORK O VALOR ARMAZENDO EM ADRESH, JÁ QUE O TESTE ANTERIOR AFETOU O VALOR ARMAZENADO EM WORK
	SUBLW	.208		;SE WORK FOR MENOR QUE 208, O CARRY É SETADO. CASO CONTRÁRIO, ELE É LIMPADO.
