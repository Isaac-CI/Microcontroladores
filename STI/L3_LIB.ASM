;* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * *
;*                 ROTINA DE SAÍDA DA INTERRUPÇÃO                  *
;* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * *
; OS VALORES DE "W" E "STATUS" DEVEM SER RECUPERADOS ANTES DE 
; RETORNAR DA INTERRUPÇÃO

SAI_INT
	SWAPF	STATUS_TEMP,W
	MOVWF	STATUS		;MOVE STATUS_TEMP PARA STATUS
	SWAPF	W_TEMP,F
	SWAPF	W_TEMP,W	;MOVE W_TEMP PARA W
	RETFIE

;* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * *
;*	            	 ROTINAS E SUBROTINAS                      *
;* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * *
; CADA ROTINA OU SUBROTINA DEVE POSSUIR A DESCRIÇÃO DE FUNCIONAMENTO
; E UM NOME COERENTE ÀS SUAS FUNÇÕES.
LE_EEPROM
;LER DADO DA EEPROM, CUJO ENDEREÇO É INDICADO EM W
;O DADO LIDO RETORNA EM W
	ANDLW	.127		;LIMITA ENDEREÇO MAX. 127
	BANK1			;ACESSO VIA BANK 1
	MOVWF	EEADR		;INDICA O END. DE LEITURA
	BSF	EECON1,RD	;INICIA O PROCESSO DE LEITURA
	MOVF	EEDATA,W	;COLOCA DADO LIDO EM W
	BANK0			;POSICIONA PARA BANK 0
	RETURN

GRAVA_EEPROM
;ESCREVE UM DADO NA EEPROM, CUJO ENDEREÇO É INDICADO EM W
;O DADO A SER ESCRITO DEVE ESTAR ARMAZENADO NA VARIÁVEL DADO
	ANDLW	.127		;LIMITA ENDEREÇO MAX. 127
	BANK1			;ACESSO VIA BANK 1
	MOVWF	EEADR
	MOVF	DADO,W
	MOVWF	EEDATA
	BSF	EECON1,WREN	;HABILITA ESCRITA
	BCF	INTCON,GIE	;DESLIGA INTERRUPÇÕES
	MOVLW	B'01010101'	;DESBLOQUEIA ESCRITA
	MOVWF	EECON2		;
	MOVLW	B'10101010'	;DESBLOQUEIA ESCRITA
	MOVWF	EECON2		;
	BSF	EECON1,WR	;INICIA A ESCRITA
AGUARDA
	BTFSC	EECON1,WR	;TERMINOU?
	GOTO	AGUARDA
	BSF	INTCON,GIE	;HABILITA INTERRUPÇÕES
	BANK0			;POSICIONA PARA BANK 0
	RETURN

	
WAIT_92
;A SUBROTINA WAIT_92 É RESPONSÁVEL POR FAZER O PROGRAMA ESPERAR POR 92 CICLOS DE
;INSTRUÇÃO A FIM DE SINCRONIZAR O ENVIO DO PRIMEIRO BIT NA FUNÇÃO SEND_BYTE. 
	MOVLW	.173
	MOVWF	TMR0
	BCF	INTCON,2
	NOP			;NOP PARA SINCRONIA
	NOP			;NOP PARA SINCRONIA
    LOOP_92
	BTFSS	INTCON,2
	GOTO	LOOP_92
	
	RETURN
	
WAIT_96
;A SUBROTINA WAIT_96 É RESPONSÁVEL POR FAZER O PROGRAMA ESPERAR POR 96 CICLOS DE
;INSTRUÇÃO A FIM DE SINCRONIZAR O ENVIO DOS BITS NA FUNÇÃO SEND_BYTE.
	
	MOVLW	.170
	MOVWF	TMR0
	BCF	INTCON,2
	NOP			;NOP PARA SINCRONIA
	NOP			;NOP PARA SINCRONIA	
    LOOP_96
	BTFSS	INTCON,2
	GOTO	LOOP_96
	NOP			;NOP PARA SINCRONIA
	
	RETURN
	
WAIT_87
;A SUBROTINA WAIT_87 É RESPONSÁVEL POR FAZER O PROGRAMA ESPERAR POR 87 CICLOS DE
;INSTRUÇÃO A FIM DE SINCRONIZAR O ENVIO DOS BITS NA FUNÇÃO RECEIVE_PACKET.
	
	MOVLW	.179
	MOVWF	TMR0
	BCF	INTCON,2
	NOP			;NOP PARA SINCRONIA
	NOP			;NOP PARA SINCRONIA	
    LOOP_96
	BTFSS	INTCON,2
	GOTO	LOOP_96
	NOP			;NOP PARA SINCRONIA
	
	RETURN
	
WAIT_90
;A SUBROTINA WAIT_90 É RESPONSÁVEL POR FAZER O PROGRAMA ESPERAR POR 90 CICLOS DE
;INSTRUÇÃO A FIM DE SINCRONIZAR O RECEBIMENTO DOS BITS NA FUNÇÃO RECEIVE_PACKET.
;A FIM DE EVITAR QUE SEJA PERDIDO TEMPO EM RECEIVE_PACKET QUANDO SE TROCA DE BYTE A SER RECEBIDO,
;ASSIM QUE O VALOR EM TIMER É ATUALIZADO, VERIFICA SE COUNTER É 0, OU SEJA, SE ACABOU DE SAIR DE UM BYTE.
;CASO SEJA, COUNTER RECEBE 8, DE FORMA COM QUE SINALIZE O INÍCIO DO RECEBIMENTO DE UM NOVO BYTE. ALÉM DISSO,
;O TEMPO ESPERADO QUANDO SE TROCA DE BYTE É DIFERENTE DO TEMPO ESPERADO QUANDO O BYTE É O MESMO.
	
	MOVLW	.176
	MOVWF	TMR0
	BCF	INTCON,2
	MOVLW	.8
	MOVF	COUNTER,1
	BTFSC	STATUS,2	;CASO COUNTER SEJA 0
	MOVWF	COUNTER		;O VALOR DE COUNTER É ALTERADO PARA 8
	NOP			;NOP PARA SINCRONIA
    LOOP_90
	BTFSS	INTCON,2
	GOTO	LOOP_90
	NOP			;NOP PARA SINCRONIA
	
	RETURN
	
SEND_BYTE
;FUNÇÃO RESPONSÁVEL POR ENVIAR UM BYTE ARMAZENADO NA VARIÁVEL DADO POR COMUNICAÇÃO
;SERIAL POR MEIO DO PROTOCOLO RS232 MODIFICADO PARA VALORES DE TENSÃO COMPATÍVEIS
;COM O PIC12F675 NUMA TAXA DE TRANSMISSÃO DE APROXIMADAMENTE 9600 BAUDS.
	
	CALL	WAIT_92
	MOVLW	.8
	MOVWF	COUNTER
    
    _NEXT
	BTFSC	DADO, 0		;VERIFICA O BIT MENOS SIGNIFICANTE DE DADO
	GOTO	_HIGH		;CASO O LSB DE WORD SEJA 1, PULA ATÉ A LABEL HIGH
	NOP			;NOP PARA SINCRONIA
	BCF	GPIO, 5		;LIMPA  GP5, TRANSMITINDO O VALOR 0
	CALL	WAIT_96		;CHAMA WAIT_96 PARA SINCRONIZAR O TEMPO DE TRANSMISSÃO DO BIT DE ACORDO COM A TAXA DE TRANSFERÊNCIA
		
	RRF	DADO, 1		;ROTACIONA 1, PARA QUE O NOVO LSB DE DADO, QUE SERÁ VERFICADO NA PROXIMA ITERAÇÃO, ASSUMA O VALOR CORRETO.
	DECFSZ	COUNTER		;DECREMETNA COUNTER, DE FORMA COM QUE ELE INDIQUE QUANTAS ITERAÇÕES FALTAM PARA O TERMINO DO ENVIO DO SIMBOLO.
	GOTO	_NEXT		;CASO COUNTER NÃO SEJA 0, VAI PARA A PROXIMA ITERAÇÃO
	
	RETURN
	
    _HIGH
	BSF	GPIO, 5		;"SETTA" O BIT DE SAÍDA CASO O VALOR DO LSB DE WORD SEJA 1.
	CALL	WAIT_96		;CHAMA WAIT_96 PARA SINCRONIZAR O TEMPO DE TRANSMISSÃO DO BIT DE ACORDO COM A TAXA DE TRANSFERÊNCIA
		
	RRF	WORD, 1		;ROTACIONA 1, PARA QUE O NOVO LSB DE WORD, QUE SERÁ VERFICADO NA PROXIMA ITERAÇÃO, ASSUMA O VALOR CORRETO.
	DECFSZ	COUNTER		;DECREMETNA COUNTER, DE FORMA COM QUE ELE INDIQUE QUANTAS ITERAÇÕES FALTAM PARA O TERMINO DO ENVIO DO SIMBOLO.
	GOTO	_NEXT		;CASO COUNTER NÃO SEJA 0, VAI PARA A PROXIMA ITERAÇÃO
	
	RETURN
	
SEND_PACKET
;FUNÇÃO RESPONSÁVEL POR ENCAPSULAR E ENVIAR UM PACOTE DO PROTOCOLO L3.
;CERTIFIQUE-SE DE QUE O BYTE DE INÍCIO DE TRANSMISSÃO B'00000001' ESTÁ ARMAXENADO EM SOH,
;QUE O IDENTIFICADOR DO DISPOSITIVO DE ORIGEM ESTEJA ARMAZENADO EM IDS,
;QUE O IDENTIFICADOR DE DISPOSITIVO DE DESTINO ESTEJA ARMAZENADO EM IDD,
;QUE O CÓDIGO DA INSTRUÇÃO DO PACOTE ESTEJA ARMAZENADO EM COD, 
;QUE O IDENTIFICADOR DE FRAGMENTAÇÃO/PADDING ESTEJA EM PAD,
;E QUE OS DADOS DO PAYLOAD ESTEJAM ARMAZENADOS NOS 6 ENDEREÇOS DA MEMÓRIA A PARTIR DO ENDEREÇO DO VALOR ARMAZENADO EM DATA_ADDR
	
	MOVF	DATA_ADDR,W	;CARREGA O ENDEREÇO DO PRIMEIRO BYTE DO PACOTE EM WORK
	MOVWF	FSR		;FSR APONTA PARA O ENDEREÇO DO PRIMEIRO BYTE DO PACOTE A SER ENVIADO
	MOVLW	.12		
	MOVWF	SIZE		;SIZE RECEBE O TAMANHO DO PACOTE A SER ENVIADO, QUE É DE 12 BYTES
	CLRF	CRC		;LIMPA CRC PARA QUE ESTE SEJA PREENCHIDO CORRETAMENTE
	
    ITER_FSR
	MOVF	INDF,W		;MOVE O VALOR ARMAZENADO NO ENDEREÇO APONTADO POR FSR PARA WORK
	MOVWF	DADO		;MOVE O DOS DADOS A SEREM ENVIADOS PARA DADO
	CALL	SEND_BYTE	;CHAMA A SUBROTINA DE ENVIO DE BYTE
	MOVF	DADO,W		;MOVE O VALOR DO BYTE ENVIADO PARA WORK, DE FORMA COM QUE ELE POSSA SER ADICIONADO À CRC
	ADDWF	CRC,1		;SOMA O VALOR DO DADO A SER ENVIADO EM CRC, PARA HABILITAR A CHECAGEM DE ERROS. ELE É ADICIONADO APÓS O ENVIO DO BYTE PARA EVITAR QUE CRC SEJA SOMADO CONSIGO MESMO ANTES DE SER ENVIADO.
	INCF	FSR		;INCREMENTA O VALOR EM FSR, PARA QUE ESTE APONTE PARA A PRÓXIMA POSIÇÃO NA MEMÓRIA
	DECFSZ	SIZE		;DECREMENTA SIZE. CASO O RESULTADO SEJA 0, SAI DO LOOP DE REPETIÇÃO
	GOTO	ITER_FSR	;CASO SIZE NÃO SEJA 0, VOLTA AO LOOP PARA EXTRAIR OS VALORES DA MEMÓRIA E ENVIÁ-LOS POR COMUNICAÇÃO SERIAL
	
	RETURN
	
RECEIVE_NAK
;FUNÇÃO RESPONSÁVEL POR REENVIAR O PACOTE QUE GEROU NAK CASO O ERRO INDIQUE QUE ELE DEVE SER RE-ENVIADO.

	MOVF	DATA_1,W
	SUBLW	.2
	BTFSC	STATUS,2
	RETURN
	
	MOVF	DATA_1,W
	SUBLW	.3
	BTFSC	STATUS,2
	RETURN
	
	MOVF	DATA_1,W
	SUBLW	.3
	BTFSC	STATUS,2
	RETURN
	
	MOVF	DATA_1,W
	SUBLW	.4
	BTFSC	STATUS,2
	RETURN
	
	MOVF	DATA_1,W
	SUBLW	.5
	BTFSC	STATUS,2
	RETURN
	
	MOVF	DATA_2,W
	SUBLW	.5
	BTFSC	STATUS,2
	CALL	SEND_ENQ
	
	MOVF	DATA_2,W
	SUBLW	.6
	BTFSC	STATUS,2
	CALL	SEND_ACK
	
	MOVF	DATA_2,W
	SUBLW	.21
	BTFSC	STATUS,2
	CALL	SEND_NAK
	
	MOVF	DATA_2,W
	SUBLW	.84
	BTFSC	STATUS,2
	CALL	SEND_SMS
	
	MOVF	DATA_2,W
	SUBLW	.86
	BTFSC	STATUS,2
	CALL	SEND_RMS
	
	CLRW
	RETURN
	
RECEIVE_RMS
;FUNÇÃO QUE PROCESSA OS DADOS RECEBIDOS NO PACOTE RMS, GUARDANDO OS VALORES DAS MEDIÇÕES E O ID DO SENSOR NA MEMÓRIA
	MOVF	DATA_1,W
	MOVWF	RMS_S		;SALVA DATA_1, QUE CONTÉM O ID DO SENSOR, EM RMS_S
	MOVF	DATA_2,W
	MOVWF	RMS_I		;SALVA DATA_2, QUE CONTÉM A PARTE INTEIRA DA MEDIÇÃO DO SENSOR EM RMS_I
	MOVF	DATA_3,W
	MOVWF	RMS_F		;SALVA DATA_3, QUE CONTÉM A PARTE FRACIONÁRIA DA MEDIÇÃO DO SENSOR EM RMS_F
	
	CLRW			;LIMPA WORK PARA RETORNAR 0
	RETURN			;RETORNA 0
	
RECEIVE_PACKET
;FUNÇÃO RESPONSÁVEL POR RECEBER E DESENCAPSULAR UM PACOTE DO PROTOCOLO L3.
;OS BYTES DO PACOTE RECEBIDO SÃO GUARDADOS EM SUAS RESPECTIVAS VARIÁVEIS E
;A RESPOSTA ADEQUADA É DEVIDAMENTE ENCAPSULADA E ENVIADA.
	
	BANK0
	CLRF	COUNTER		;LIMPA COUNTER
	MOVF	DATA_ADDR,W	;MOVE O ENDEREÇO DOS DADOS PARA WORK
	MOVWF	FSR		;MOVE O ENDEREÇO DO PRIMEIRO BYTE DE DADOS PARA FSR
	CLRF	SOH		;LIMPA SOH
	CLRF	IDS		;LIMPA IDS
	CLRF	IDD		;LIMPA IDD
	MOVF	COD,W		;MOVE COD PARA WORK
	CLRF	COD		;LIMPA COD
	CLRF	DATA_1		;LIMPA DATA_1
	CLRF	DATA_2		;LIMPA DATA_2
	CLRF	DATA_3		;LIMPA DATA_3
	CLRF	DATA_4		;LIMPA DATA_4
	CLRF	DATA_5		;LIMPA DATA_5
	CLRF	DATA_6		;LIMPA DATA_6
	CLRF	PAD		;LIMPA PAD
	CLRF	CRC		;LIMPA CRC
	
	MOVLW	.12
	MOVWF	SIZE
    LOOP_DATA
	RRF	INDF,1		;ROTACIONA O CONTEÚDO DE INDF PARA A DIREITA, DE MODO COM QUE O PRIMEIRO BIT RECEBIDO SEJA O LSB E O ULTIMO O MSB
	BCF	INDF,7		;LIMPA O MSB DE INDF
	BTFSC	GPIO,GP2	;CASO GP2 ESTEJA NO NÍVEL BAIXO, O MSB DE COD PERMANECE LIMPO
	BSF	INDF,7		;CASO CONTRÁRIO, ELE É SETTADO
	
	MOVLW	.1
	SUBWF	COUNTER,W
	BTFSS	STATUS,2	;SE COUNTER FOR 1
	CALL	WAIT_87		;ESPERA 87 CICLOS DE INSTRUÇÃO
	
	MOVLW	.1
	SUBWF	COUNTER,W	
	BTFSC	STATUS,2	;SE COUNTER FOR DIFERENTE DE 1
	CALL	WAIT_90		;ESPERA 90 CICLOS DE INSTRUÇÃO, ALTERANDO O VALOR DE COUNTER PARA 8 CASO COUNTER SEJA 0
	
	DECFSZ	COUNTER		;DECREMENTA COUNTER E VEERIFICA SE ELE ASSUMIU O VALOR 0
	GOTO	LOOP_DATA	;CASO COUNTER SEJA MAIOR QUE 0, VOLTA PARA O LOOP DE LEITURA DOS BITS DE DATA
	INCF	FSR		;INCREMENTA FSR PARA QUE ELE APONTE PARA O PRÓXIMO ELEMENTO DE DATA
	DECFSZ	SIZE		;DECREMENTA SIZE, PARA VERIFICAR QUANTOS BYTES DE DADOS AINDA FALTAM SEREM LIDOS
	GOTO	LOOP_DATA	;CASO SIZE SEJA MAIOR QUE 0, VOLTA PARA O INÍCIO DO LOOP, SETANDO O CONTADOR PARA LER MAIS UM BYTE
	
	CLRF	ERR		;LIMPA ERR
    CHECK_CRC
	INCF	ERR		;ERR = 1, INDICANDO ERRO DE CRC
	MOVF	DATA_ADDR,W
	MOVWF	FSR		;FAZ FSR APONTAR PARA O PRIMEIRO ELEMENTO DE DATA
